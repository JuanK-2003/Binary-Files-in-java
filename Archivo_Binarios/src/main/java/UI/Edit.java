    boolean validateFields() {
        return !jTextField2.getText().isEmpty() &&
                !jTextField3.getText().isEmpty();
    }
    
    void setDataInTF(int idx){
        jTextField2.setText(p.get(idx).getNombre());
        jTextField3.setText(Integer.toString(p.get(idx).getEdad()));
    }
    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
            if ( validateFields() ) {
            p.get(i).editperson(
                jTextField2.getText(),
                Integer.parseInt(jTextField3.getText())
            );
            JOptionPane.showMessageDialog(rootPane, "\t Datos actualizados :) ");
        }
        clearFields();
    }//GEN-LAST:event_editButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        i = Search.searchByID(jTextField1.getText());
        if ( i >= 0 ) {
            setDataInTF(i);
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "\t El usuario N O  E X I S T E ");
        }
    }//GEN-LAST:event_searchButtonActionPerformed


